import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.dirname(fileURLToPath(import.meta.url));
const safeDir = path.join(rootDir, "safe");
const canonicalSiteBase = "https://ujala-portfolio.onrender.com";
const publicSiteBase = cleanPublicSiteBase(process.env.PUBLIC_SITE_BASE);

function cleanPublicSiteBase(value) {
  const candidate = (value || canonicalSiteBase).replace(/\/+$/, "");
  try {
    const url = new URL(candidate);
    if (url.protocol === "https:" && url.hostname === "ujala-portfolio.onrender.com") {
      return candidate;
    }
  } catch {
    // Fall through to the repo-owned canonical URL.
  }

  return canonicalSiteBase;
}

const sourcePages = [
  "index.html",
  "work.html",
  "systems.html",
  "about.html",
  "playground.html",
  "contact.html",
];

function safePublicUrl(page) {
  return `${publicSiteBase}/safe/${page}`;
}

function escapeScript(value) {
  return value.replaceAll("</script>", "<\\/script>");
}

function stripExternalHeadAssets(html) {
  return html
    .replace(/\s*<meta http-equiv="Content-Security-Policy"[^>]+>/g, "")
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.googleapis\.com">/g, "")
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.gstatic\.com" crossorigin>/g, "")
    .replace(/\s*<link href="https:\/\/fonts\.googleapis\.com[^>]+>/g, "");
}

function normalizePublicUrls(html, page) {
  const url = safePublicUrl(page);
  return html
    .replace(/<link rel="canonical" href="[^"]+">/, `<link rel="canonical" href="${url}">`)
    .replace(/<meta property="og:url" content="[^"]+">/, `<meta property="og:url" content="${url}">`);
}

function rewriteStaticRouteLinks(html) {
  return html.replace(/href="(index|work|systems|about|playground|contact)\.html([?#][^"]*)?"/g, (_match, pageName, suffix = "") => {
    return `href="${safePublicUrl(`${pageName}.html${suffix}`)}"`;
  });
}

function safePortfolioDataScript(source) {
  return source.replaceAll("content/branding/", "../content/branding/");
}

function safeStylesheet(source) {
  return source.replaceAll('url("content/', 'url("../content/');
}

function scriptTag(source) {
  return `<script>\n${escapeScript(source)}\n</script>`;
}

async function readText(relativePath) {
  const text = await readFile(path.join(rootDir, relativePath), "utf8");
  return text.replace(/\r\n/g, "\n");
}

async function main() {
  await mkdir(safeDir, { recursive: true });

  const [styles, portfolioData, runtimeData, appCore, appRender, visualUpgrades, appMain] = await Promise.all([
    readText("styles.css"),
    readText("portfolio-data.js"),
    readText("portfolio-runtime.json"),
    readText("app-core.js"),
    readText("app-render.js"),
    readText("visual-upgrades.js"),
    readText("app-main.js"),
  ]);

  const styleBlock = `<style>\n${safeStylesheet(styles)}\n</style>`;
  const scriptBlock = [
    scriptTag(safePortfolioDataScript(portfolioData)),
    scriptTag(`window.UJOS_RUNTIME_DATA = ${runtimeData.trim()};`),
    scriptTag(appCore),
    scriptTag(appRender),
    scriptTag(visualUpgrades),
    scriptTag(appMain),
  ].join("\n");

  for (const page of sourcePages) {
    const sourcePath = path.join(rootDir, page);
    const targetPath = path.join(safeDir, page);
    let html = await readFile(sourcePath, "utf8");
    html = stripExternalHeadAssets(html);
    html = normalizePublicUrls(html, page);
    html = rewriteStaticRouteLinks(html);
    html = html.replace(/\s*<link rel="stylesheet" href="styles\.css">/, `\n${styleBlock}`);
    html = html.replace(
      /\s*<script(?: defer)? src="portfolio-data\.js"><\/script>\s*<script(?: defer)? src="(?:script|app-core|app-render|visual-upgrades|app-main)\.js"><\/script>(?:\s*<script(?: defer)? src="(?:app-core|app-render|visual-upgrades|app-main)\.js"><\/script>)*/,
      ""
    );
    html = html.replace(/\s*<\/body>/, `\n${scriptBlock}\n</body>`);
    html = html.replace("<html lang=\"en\">", "<html lang=\"en\">\n<!-- Generated by build-safe-mirror.mjs. Edit source pages/assets, then rebuild. -->");
    await writeFile(targetPath, html, "utf8");
  }

  console.log(`safe mirror generated: ${sourcePages.length} pages`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
