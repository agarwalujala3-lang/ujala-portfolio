import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.dirname(fileURLToPath(import.meta.url));
const safeDir = path.join(rootDir, "safe");
const githubRawBase = "https://raw.githubusercontent.com/agarwalujala3-lang/ujala-portfolio/main";
const htmlPreviewBase = "https://htmlpreview.github.io/?";

const sourcePages = [
  "index.html",
  "work.html",
  "systems.html",
  "about.html",
  "playground.html",
  "contact.html",
];

function safePublicUrl(page) {
  return `${htmlPreviewBase}${githubRawBase}/safe/${page}`;
}

function escapeScript(value) {
  return value.replaceAll("</script>", "<\\/script>");
}

function stripExternalHeadAssets(html) {
  return html
    .replace(/\s*<meta http-equiv="Content-Security-Policy"[^>]+>/g, "")
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.googleapis\.com">/g, "")
    .replace(/\s*<link rel="preconnect" href="https:\/\/fonts\.gstatic\.com" crossorigin>/g, "")
    .replace(/\s*<link href="https:\/\/fonts\.googleapis\.com[^>]+>/g, "");
}

function normalizePublicUrls(html, page) {
  const url = safePublicUrl(page);
  return html
    .replace(/<link rel="canonical" href="[^"]+">/, `<link rel="canonical" href="${url}">`)
    .replace(/<meta property="og:url" content="[^"]+">/, `<meta property="og:url" content="${url}">`);
}

function safePortfolioDataScript(source) {
  return source.replaceAll("content/branding/", "../content/branding/");
}

function scriptTag(source) {
  return `<script>\n${escapeScript(source)}\n</script>`;
}

async function readText(relativePath) {
  return readFile(path.join(rootDir, relativePath), "utf8");
}

async function main() {
  await mkdir(safeDir, { recursive: true });

  const [styles, portfolioData, runtimeData, appCore, appRender, appMain] = await Promise.all([
    readText("styles.css"),
    readText("portfolio-data.js"),
    readText("portfolio-runtime.json"),
    readText("app-core.js"),
    readText("app-render.js"),
    readText("app-main.js"),
  ]);

  const styleBlock = `<style>\n${styles}\n</style>`;
  const scriptBlock = [
    scriptTag(safePortfolioDataScript(portfolioData)),
    scriptTag(`window.UJOS_RUNTIME_DATA = ${runtimeData.trim()};`),
    scriptTag(appCore),
    scriptTag(appRender),
    scriptTag(appMain),
  ].join("\n");

  for (const page of sourcePages) {
    const sourcePath = path.join(rootDir, page);
    const targetPath = path.join(safeDir, page);
    let html = await readFile(sourcePath, "utf8");
    html = stripExternalHeadAssets(html);
    html = normalizePublicUrls(html, page);
    html = html.replace(/\s*<link rel="stylesheet" href="styles\.css">/, `\n${styleBlock}`);
    html = html.replace(
      /\s*<script src="portfolio-data\.js"><\/script>\s*<script src="script\.js"><\/script>/,
      `\n${scriptBlock}`
    );
    html = html.replace("<html lang=\"en\">", "<html lang=\"en\">\n<!-- Generated by build-safe-mirror.mjs. Edit source pages/assets, then rebuild. -->");
    await writeFile(targetPath, html, "utf8");
  }

  console.log(`safe mirror generated: ${sourcePages.length} pages`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});

